//讲一个正整数分解质因数例如：输入90，打印出90=2*3*3*5;90=2*45=2*3*15=2*3*3*5
//90/2=45,质数只能被自身和1整除，
//某数s，i=2,i++，s%i==0 s就不是质数，保留x=s%i==0的x,在从i=2循环
//
var i=2;
var s=90;
var y='';
for(i=2;i<=s;i++){//15=3*5  //3=15/5
	if(s%i==0){
		s=s/i;       
		var x=i;
		if(s<=i){
		var y=y+x+i+'*';
		}else{
	    var y=y+x+'*';
		}
	}
}
console.log(y);

